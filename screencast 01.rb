# The difference between the following:

# "h" + "a" * 10

# ("h" + "a") * 10

# is which letter the 10 is applied to. In the first instance, the 10 only 
# applies to the "a", giving us the output "haaaaaaaaaa". In the second, the 
# 10 is applied to both letters, giving us the output "hahahahahahahahahaha". 
# The parentheses in the second example alters the order of operations for the
# concatenation of the strings.